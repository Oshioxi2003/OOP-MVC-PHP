                    <h3>1. Lừa Đảo Mua Bán Đất Bằng Giấy Tờ Giả</h3>



                    <div class="p">Những đối tượng lừa đảo mạo danh chủ sở hữu, dùng giấy tờ được làm giả rồi bán nhà
                        đất cho
                        người có nhu cầu. Những đối tượng này trước đó đã đóng giả làm người mua để tiếp cận người bán,
